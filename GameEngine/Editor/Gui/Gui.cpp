#include "Gui.h"

void Gui::Update(std::shared_ptr<Scene> scene)
{
    ViewportPanel::Update(scene);
}

void Gui::PreRender()
{
    ImGui_ImplOpenGL3_NewFrame();
    ImGui_ImplGlfw_NewFrame();
    ImGui::NewFrame();
}

void Gui::PostRender()
{
    ImGui::Render();
    ImGui_ImplOpenGL3_RenderDrawData(ImGui::GetDrawData());

    if (ImGui::GetIO().ConfigFlags & ImGuiConfigFlags_ViewportsEnable)
    {
        GLFWwindow* backup_current_context = glfwGetCurrentContext();
        ImGui::UpdatePlatformWindows();
        ImGui::RenderPlatformWindowsDefault();
        glfwMakeContextCurrent(backup_current_context);
    }
}

void Gui::Render(std::shared_ptr<Scene> scene)
{
    PreRender();
    RenderDockSpace();
    ViewportPanel::Render(scene);
    EntitiesPanel::Render(scene);
    ComponentPanel::Render(scene);
    SettingsPanel::Render(scene);
    FilesystemPanel::Render();
    ImGui::ShowDemoWindow();

    ImGui::Begin("Teszt");

    float width = 100;
    float height = 150;

    ImGui::PushStyleVar(ImGuiStyleVar_CellPadding, ImVec2(0, 0));
    ImGui::PushStyleColor(ImGuiCol_BorderShadow, ImVec4(1, 1, 0, 1));
    ImGui::PushStyleColor(ImGuiCol_TableBorderStrong, ImVec4(0, 0, 0, 1));
    ImGui::PushStyleColor(ImGuiCol_TableRowBg, ImVec4(0, 0, 0, 1));
    ImGui::BeginTable("TesztTable", 1, ImGuiTableFlags_BordersOuter, ImVec2(width, height));

    ImGui::TableNextRow();
    ImGui::TableSetColumnIndex(0);
    ImGui::Image(0, ImVec2(width, height * 0.67));

    ImGui::TableNextRow();
    ImGui::TableSetColumnIndex(0);

    std::string text = "This is a longer text for sure";
    auto contentSize = ImVec2(width, height * 0.33);
    auto textSize = ImGui::CalcTextSize(text.c_str());

    ImGui::SetCursorPosX(ImGui::GetCursorPosX() + (contentSize.x - textSize.x) * 0.5);
    ImGui::SetCursorPosY(ImGui::GetCursorPosY() + (contentSize.y - textSize.y) * 0.5);
    ImGui::TextWrapped(text.c_str());

    ImGui::EndTable();
    ImGui::PopStyleVar(1);
    ImGui::PopStyleColor(3);

    ImGui::End();


    PostRender();
}

void Gui::RenderDockSpace()
{
    static bool opt_fullscreen = true;
    static bool opt_padding = false;
    static ImGuiDockNodeFlags dockspace_flags = ImGuiDockNodeFlags_None;

    ImGuiWindowFlags window_flags = ImGuiWindowFlags_NoDocking;
    if (opt_fullscreen)
    {
        const ImGuiViewport* viewport = ImGui::GetMainViewport();
        ImGui::SetNextWindowPos(viewport->WorkPos);
        ImGui::SetNextWindowSize(viewport->WorkSize);
        ImGui::SetNextWindowViewport(viewport->ID);
        ImGui::PushStyleVar(ImGuiStyleVar_WindowRounding, 0.0f);
        ImGui::PushStyleVar(ImGuiStyleVar_WindowBorderSize, 0.0f);
        window_flags |= ImGuiWindowFlags_NoTitleBar | ImGuiWindowFlags_NoCollapse | ImGuiWindowFlags_NoResize | ImGuiWindowFlags_NoMove;
        window_flags |= ImGuiWindowFlags_NoBringToFrontOnFocus | ImGuiWindowFlags_NoNavFocus;
    }
    else
    {
        dockspace_flags &= ~ImGuiDockNodeFlags_PassthruCentralNode;
    }

    if (dockspace_flags & ImGuiDockNodeFlags_PassthruCentralNode)
        window_flags |= ImGuiWindowFlags_NoBackground;

    if (!opt_padding)
        ImGui::PushStyleVar(ImGuiStyleVar_WindowPadding, ImVec2(0.0f, 0.0f));

    ImGui::Begin("DockSpace", nullptr, window_flags);

    if (!opt_padding)
        ImGui::PopStyleVar();

    if (opt_fullscreen)
        ImGui::PopStyleVar(2);

    // Submit the DockSpace
    ImGuiIO& io = ImGui::GetIO();
    if (io.ConfigFlags & ImGuiConfigFlags_DockingEnable)
    {
        ImGuiID dockspace_id = ImGui::GetID("MyDockSpace");
        ImGui::DockSpace(dockspace_id, ImVec2(0.0f, 0.0f), dockspace_flags);
    }

    if (ImGui::BeginMainMenuBar())
    {
        if (ImGui::BeginMenu("File"))
        {
            if (ImGui::MenuItem("Load"))
            {

            }
            if (ImGui::MenuItem("Save"))
            {

            }

            ImGui::EndMenu();
        }

        ImGui::EndMainMenuBar();
    }

    ImGui::End();
}

